function toArray(pathDescription) {
	return pathDescription.split(/[^0-9,.]/).filter(x=>x.length !=0).map(x=>x.split(/\,/));
}

function printArray(array) {
	return '['+array.map(p=>'['+p.join(',')+']').join(',')+']';
}
